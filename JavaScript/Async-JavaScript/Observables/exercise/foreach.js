// 1 - print names in an array
function(console) {
  var names = ["Ben", "Jafar", "Matt", "Priya", "Brian"], counter;

  for(counter = 0; counter < names.length; counter++) {
    console.log(names[counter]);
  }
}

// 2 - Using For each
function(console) {
	var names = ["Ben", "Jafar", "Matt", "Priya", "Brian"];

	names.forEach(function(name) {
		console.log(name);
	});
}
    
